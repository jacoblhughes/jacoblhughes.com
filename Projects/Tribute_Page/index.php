                <ul>
                    <li class="odd">
                        <strong>Creation</strong> - The idea of the modern Pop-Tarts was created by Post. Originally called "Country Squares", they were meant as a compliment to Post's cold cereals.
                    </li>
                    <br>
                    <li class="even">
                        <strong>Competition</strong> - Due to the "Country Squares" being advertised but not ready to be put in the marketplace, the Kellog Company was able to develop a competing version in six months.
                    </li>
                    <br>
                    <li class="odd">
                        <strong>Flavors</strong> - The original flavors were strawberry, blueberry, brown sugar cinnamon, and apple currant. Frosting was not introduced until 1967, three years after Pop-Tarts were introduced. As of 2016, flavors include hot fudge sundae, s'mores, raspberry, and peanut butter.
                    </li>
                    <br>
                    <li class="even">
                        <strong>Litigation</strong> - Strawberry Pop-Tarts have been a source of heated lawsuits. It was discovered in 1994 that strawberry Pop-Tarts, when left in the toaster oven for too long, could produce a flame of over a foot high!
                    </li>
                    <br>
                    <li class="odd">
                        <strong>Current Sales</strong> - As of 2014, sales of Pop-Tarts had increased for 32 straight years.
                    </li>
                    <br>
                    <li class="even">
                        <strong>Nutritional Value</strong> - Each Pop-Tart contains a minimum of 14 grams of sugar, meaning that <strong>two</strong> Pop-Tarts consumes all of the daily allowances in men, woman, and children!!
                    </li>
                </ul>
